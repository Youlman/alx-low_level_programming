#include <stdio.h>
#include "3-calc.h"

/**
* op_add - the sum of two integers
* @a: the first integer
* @b: the second integer
*
* Return: the sum
*/

int op_add(int a, int b)
{
	return (a + b);
}


/**
* op_sub - the difference of two integers
* @a: the first integer
* @b: the second integer
*
* Return: the difference
*/

int op_sub(int a, int b)
{
	return (a - b);
}


/**
* op_mul - the product of two integers
* @a: the first integer
* @b: the second integer
*
* Return: the product
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - the result of division of two integers
* @a: the first integer
* @b: the second integer
*
* Return: the result of division
*/

int op_div(int a, int b)
{
	return (a / b);
}


/**
* op_mod - the remainder of the division of two integers
* @a: the first integer
* @b: the second integer
*
* Return: the remainder of the division
*/

int op_mod(int a, int b)
{
	return (a % b);
}

