#include "main.h"

/**
* add - Add two integers
* @a: The first integer
* @b: The second integer
* Return: sum
*/
int add(int a, int b)
{
	int sum = a + b;

	return (sum);
}


/**
* sub - Difference two integers
* @a: The first integer
* @b: The second integer
* Return: sub
*/
int sub(int a, int b)
{
	int sub = a - b;

	return (sub);
}


/**
* mul - Product two integers
* @a: The first integer
* @b: The second integer
* Return: mul
*/
int mul(int a, int b)
{
	int mul = a * b;

	return (mul);
}


/**
* div - Division two integers
* @a: The first integer
* @b: The second integer
* Return: div
*/
int div(int a, int b)
{
	int div = a / b;

	return (div);
}

/**
* mod - modulo two integers
* @a: The first integer
* @b: The second integer
* Return: mod
*/
int mod(int a, int b)
{
	int mod = a + b;

	return (mod);
}
